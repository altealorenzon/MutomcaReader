#include "Hit.h"

//_________________________________________________________________________________________
Hit::Hit() {
    _boardID = -1;
    _chID    = -1;
    _layID   = -1;
    _cellID  = -1;
    _rawtime = -9999.;
}

//_________________________________________________________________________________________
Hit::~Hit() {

}

//_________________________________________________________________________________________
void Hit::setBoardID(int board) {
    _boardID = board;
    return;
}

//_________________________________________________________________________________________
void Hit::setChID(int ch) {
    _chID = ch;
    return;
}

//_________________________________________________________________________________________
void Hit::setLayID(int layer) {
    _layID = layer;
    return;
}

//_________________________________________________________________________________________
void Hit::setCellID(int cell) {
    _cellID = cell;
    return;
}

//_________________________________________________________________________________________
void Hit::setRawTime(float rtime) {
    _rawtime = rtime;
    return;
}

//_________________________________________________________________________________________
int Hit::getBoardID() {
    return _boardID;
}

//_________________________________________________________________________________________
int Hit::getChID() {
    return _chID;
}

//_________________________________________________________________________________________
int Hit::getLayID() {
    return _layID;
}

//_________________________________________________________________________________________
int Hit::getCellID() {
    return _cellID;
}

//_________________________________________________________________________________________
float Hit::getRawTime() {
    return _rawtime;
}
